//Difernecia entre metodos y funciones

const number1 = 20;
const number2 = "20";

console.log(parseInt(number2)); //Esto es una funcion

console.log(number1.toString()); //Esto es un metodo

//Un metodo es al final una funcion, pero que pertenece a un objeto o tipo de dato. Es el comportamiento o accion que ejecuta en POO
